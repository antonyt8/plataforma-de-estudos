window.renderPortuguesQuestions = function renderPortuguesQuestions(subjectId, containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;

  const items = (window.PortuguesQuestions || []).filter((q) => q.subjectId === subjectId);

  if (!items.length) {
    container.innerHTML = '<p class="text-slate-600">Sem questoes cadastradas para este assunto.</p>';
    return;
  }

  const html = items.map((q, idx) => {
    const options = q.options.map((opt, optIdx) => {
      return '<label class="block border border-slate-200 rounded-lg px-3 py-2 hover:bg-slate-50 cursor-pointer">' +
        '<input type="radio" name="q-' + q.id + '" value="' + optIdx + '" class="mr-2" />' +
        '<span>' + opt + '</span>' +
      '</label>';
    }).join('');

    return '<article class="bg-white border border-slate-200 rounded-xl p-4">' +
      '<h4 class="font-semibold text-slate-800 mb-3">' + (idx + 1) + '. ' + q.text + '</h4>' +
      '<div class="space-y-2">' + options + '</div>' +
      '<div class="mt-3 flex gap-2">' +
        '<button type="button" data-check="' + q.id + '" class="px-3 py-2 bg-indigo-600 text-white rounded-lg text-sm hover:bg-indigo-700">Verificar</button>' +
        '<button type="button" data-show="' + q.id + '" class="px-3 py-2 bg-slate-200 text-slate-700 rounded-lg text-sm hover:bg-slate-300">Mostrar resposta</button>' +
      '</div>' +
      '<div id="feedback-' + q.id + '" class="mt-3 text-sm"></div>' +
    '</article>';
  }).join('');

  container.innerHTML = html;

  container.querySelectorAll('[data-check]').forEach((btn) => {
    btn.addEventListener('click', () => {
      const id = Number(btn.getAttribute('data-check'));
      const q = items.find((item) => item.id === id);
      if (!q) return;
      const checked = container.querySelector('input[name="q-' + id + '"]:checked');
      const feedback = document.getElementById('feedback-' + id);
      if (!feedback) return;
      if (!checked) {
        feedback.innerHTML = '<p class="text-amber-700">Selecione uma alternativa antes de verificar.</p>';
        return;
      }
      const selected = Number(checked.value);
      if (selected === q.correctAnswer) {
        feedback.innerHTML = '<p class="text-emerald-700 font-medium">Correto!</p><p class="text-slate-600 mt-1">' + q.explanation + '</p>';
      } else {
        feedback.innerHTML = '<p class="text-red-700 font-medium">Incorreto.</p><p class="text-slate-600 mt-1">' + q.explanation + '</p>';
      }
    });
  });

  container.querySelectorAll('[data-show]').forEach((btn) => {
    btn.addEventListener('click', () => {
      const id = Number(btn.getAttribute('data-show'));
      const q = items.find((item) => item.id === id);
      const feedback = document.getElementById('feedback-' + id);
      if (!q || !feedback) return;
      feedback.innerHTML = '<p class="text-indigo-700 font-medium">Resposta: ' + (q.correctAnswer + 1) + '</p><p class="text-slate-600 mt-1">' + q.explanation + '</p>';
    });
  });
};
